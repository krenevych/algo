def Fact(n):
    if n == 0 :               # Термінальна гілка
       return 1               # Тривіальне значення
    else:                     # Рекурсивна гілка
       return n * Fact(n - 1) # Рекурсивний виклик


# Виклик рекурсивної функції
n = int(input("n = "))
print("%d! = %d" % (n, Fact(n)))
